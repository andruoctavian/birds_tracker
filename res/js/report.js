function handleLocationError(browserHasGeolocation, infoWindow, pos) {
    infoWindow.setPosition(pos);
    infoWindow.setContent(browserHasGeolocation ?
        'Error: The Geolocation service failed.' :
        'Error: Your browser doesn\'t support geolocation.');
    infoWindow.open(map);
}

var reportBird = function () {
    var select = document.getElementById("bird-select");
    var bird = select.options[select.selectedIndex].value;

    var report = {
        lat: marker.getPosition().lat(),
        lng: marker.getPosition().lng(),
        bird: bird
    };

    $.post("/src/routes/report.php", report, function (data) {
        var popup = $('#popup');
        var popupContent;
        if (data === '1') {
            popupContent = '<div class="alert alert-success alert-dismissable fade in">' +
                '<a href="#" class="close" data-dismiss="alert" aria-label="close">&times;</a>' +
                '<strong>Success!</strong> Your report has been added.' +
                '</div>';
        } else {
            popupContent = '<div class="alert alert-danger alert-dismissable fade in">' +
                '<a href="#" class="close" data-dismiss="alert" aria-label="close">&times;</a>' +
                '<strong>Failed!</strong> Your report could not been added.' +
                '</div>';
        }
        $(popup).html(popupContent);
        $(popup).fadeIn(300).delay(1000).fadeOut(400);
    });
};