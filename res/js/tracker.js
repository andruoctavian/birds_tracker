function initTrackerMap() {
    map = new google.maps.Map(document.getElementById('tracker-map'), {
        zoom: 3,
        center: {lat: 44.434, lng: 26.047},
        mapTypeControlOptions: {
            mapTypeIds: ['styled_map']
        }
    });

    //Associate the styled map with the MapTypeId and set it to display.
    map.mapTypes.set('styled_map', styledMapType);
    map.setMapTypeId('styled_map');

    // Create an array of alphabetical characters used to label the markers.
    var labels = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';
    infoWindow = new google.maps.InfoWindow;

    // Add some markers to the map.
    // Note: The code uses the JavaScript Array.prototype.map() method to
    // create an array of markers based on a given "locations" array.
    // The map() method here has nothing to do with the Google Maps API.
    var markers = reports.map(function(report, i) {
        var marker = new google.maps.Marker({
            position: report.location,
            icon: "/res/img/marker.png"
        });
        var infowincontent = "<strong>Bird</strong>: " + report.bird + "<br><strong>User</strong>: " + report.user;
        marker.addListener('click', function () {
            infoWindow.setContent(infowincontent);
            infoWindow.open(map, marker);
        });
        return marker;
    });

    // Add a marker clusterer to manage the markers.
    var markerCluster = new MarkerClusterer(map, markers,
        {imagePath: 'https://developers.google.com/maps/documentation/javascript/examples/markerclusterer/m'});
}